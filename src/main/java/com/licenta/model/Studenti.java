package com.licenta.model;


public class Studenti {
    private String idStudent;
    private String anUniversitar;
    private int varstaStudent;
    private String codPersoana;
    private int idSpecializare;

    public String getIdStudent() {
        return idStudent;
    }

    public void setIdStudent(String idStudent) {
        this.idStudent = idStudent;
    }

    public String getAnUniversitar() {
        return anUniversitar;
    }

    public void setAnUniversitar(String anUniversitar) {
        this.anUniversitar = anUniversitar;
    }

    public int getVarstaStudent() {
        return varstaStudent;
    }

    public void setVarstaStudent(int varstaStudent) {
        this.varstaStudent = varstaStudent;
    }

    public String getCodPersoana() {
        return codPersoana;
    }

    public void setCodPersoana(String codPersoana) {
        this.codPersoana = codPersoana;
    }

    public int getIdSpecializare() {
        return idSpecializare;
    }

    public void setIdSpecializare(int idSpecializare) {
        this.idSpecializare = idSpecializare;
    }
}
