package com.licenta.model;


public class Activitati {
    private int idActivitate;
    private String denumire;
    private String programul;

    public int getIdActivitate() {
        return idActivitate;
    }

    public void setIdActivitate(int idActivitate) {
        this.idActivitate = idActivitate;
    }

    public String getDenumire() {
        return denumire;
    }

    public void setDenumire(String denumire) {
        this.denumire = denumire;
    }

    public String getProgramul() {
        return programul;
    }

    public void setProgramul(String programul) {
        this.programul = programul;
    }
}
