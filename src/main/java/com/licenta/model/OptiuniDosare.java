package com.licenta.model;


public class OptiuniDosare {
    private int idOptiuniDosar;
    private int numarulOptiunii;
    private String codDosar;
    private int idAcord;

    public int getIdOptiuniDosar() {
        return idOptiuniDosar;
    }

    public void setIdOptiuniDosar(int idOptiuniDosar) {
        this.idOptiuniDosar = idOptiuniDosar;
    }

    public int getNumarulOptiunii() {
        return numarulOptiunii;
    }

    public void setNumarulOptiunii(int numarulOptiunii) {
        this.numarulOptiunii = numarulOptiunii;
    }

    public String getCodDosar() {
        return codDosar;
    }

    public void setCodDosar(String codDosar) {
        this.codDosar = codDosar;
    }

    public int getIdAcord() {
        return idAcord;
    }

    public void setIdAcord(int idAcord) {
        this.idAcord = idAcord;
    }
}
