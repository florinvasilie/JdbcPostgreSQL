package com.licenta.form;

public class FormProgramTable {
    private String denumireActivitate;
    private String programActivitate;
    private String numeMentor;
    private String detaliiProblema;
    private String metodaRezolvareProblema;

    public String getDenumireActivitate() {
        return denumireActivitate;
    }

    public void setDenumireActivitate(String denumireActivitate) {
        this.denumireActivitate = denumireActivitate;
    }

    public String getProgramActivitate() {
        return programActivitate;
    }

    public void setProgramActivitate(String programActivitate) {
        this.programActivitate = programActivitate;
    }

    public String getNumeMentor() {
        return numeMentor;
    }

    public void setNumeMentor(String numeMentor) {
        this.numeMentor = numeMentor;
    }

    public String getDetaliiProblema() {
        return detaliiProblema;
    }

    public void setDetaliiProblema(String detaliiProblema) {
        this.detaliiProblema = detaliiProblema;
    }

    public String getMetodaRezolvareProblema() {
        return metodaRezolvareProblema;
    }

    public void setMetodaRezolvareProblema(String metodaRezolvareProblema) {
        this.metodaRezolvareProblema = metodaRezolvareProblema;
    }
}
