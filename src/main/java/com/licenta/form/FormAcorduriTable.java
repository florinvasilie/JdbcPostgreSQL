package com.licenta.form;

public class FormAcorduriTable {
    private String denumirePartener;
    private String domeniuActivitate;
    private String durataAcordului;
    private int nrLocuri;
    private int valoareaAcordlui;
    private String oras;
    private String tara;

    public String getDenumirePartener() {
        return denumirePartener;
    }

    public void setDenumirePartener(String denumirePartener) {
        this.denumirePartener = denumirePartener;
    }

    public String getDomeniuActivitate() {
        return domeniuActivitate;
    }

    public void setDomeniuActivitate(String domeniuActivitate) {
        this.domeniuActivitate = domeniuActivitate;
    }

    public String getDurataAcordului() {
        return durataAcordului;
    }

    public void setDurataAcordului(String durataAcordului) {
        this.durataAcordului = durataAcordului;
    }

    public int getNrLocuri() {
        return nrLocuri;
    }

    public void setNrLocuri(int nrLocuri) {
        this.nrLocuri = nrLocuri;
    }

    public int getValoareaAcordlui() {
        return valoareaAcordlui;
    }

    public void setValoareaAcordlui(int valoareaAcordlui) {
        this.valoareaAcordlui = valoareaAcordlui;
    }

    public String getOras() {
        return oras;
    }

    public void setOras(String oras) {
        this.oras = oras;
    }

    public String getTara() {
        return tara;
    }

    public void setTara(String tara) {
        this.tara = tara;
    }
}
