package com.licenta.form;

public class FormOptiuni {
    private String nume;
    private String anUniversitarOptiuni;
    private String denumireSpecializare;
    private String dataSesiunii;

    public String getNume() {
        return nume;
    }

    public void setNume(String nume) {
        this.nume = nume;
    }

    public String getAnUniversitarOptiuni() {
        return anUniversitarOptiuni;
    }

    public void setAnUniversitarOptiuni(String anUniversitarOptiuni) {
        this.anUniversitarOptiuni = anUniversitarOptiuni;
    }

    public String getDenumireSpecializare() {
        return denumireSpecializare;
    }

    public void setDenumireSpecializare(String denumireSpecializare) {
        this.denumireSpecializare = denumireSpecializare;
    }

    public String getDataSesiunii() {
        return dataSesiunii;
    }

    public void setDataSesiunii(String dataSesiunii) {
        this.dataSesiunii = dataSesiunii;
    }
}
